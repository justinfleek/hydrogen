// | Vector types - typed coordinates in N-dimensional space.
// |
// | Re-exports all vector submodules and provides conversion functions.
import * as Data_Semiring from "../Data.Semiring/index.js";
import * as Hydrogen_Schema_Dimension_Vector_Vec2 from "../Hydrogen.Schema.Dimension.Vector.Vec2/index.js";
import * as Hydrogen_Schema_Dimension_Vector_Vec3 from "../Hydrogen.Schema.Dimension.Vector.Vec3/index.js";
import * as Hydrogen_Schema_Dimension_Vector_Vec4 from "../Hydrogen.Schema.Dimension.Vector.Vec4/index.js";
import * as Hydrogen_Schema_Dimension_Vector_VecN from "../Hydrogen.Schema.Dimension.Vector.VecN/index.js";

// | Convert Vec4 to VecN
var vec4ToVecN = function (v) {
    return [ v.value0, v.value1, v.value2, v.value3 ];
};

// | Project 4D to 3D (perspective divide if w != 1)
var vec4ToVec3 = function (v) {
    var $19 = v.value3 === 0.0;
    if ($19) {
        return new Hydrogen_Schema_Dimension_Vector_Vec3.Vec3(v.value0, v.value1, v.value2);
    };
    return new Hydrogen_Schema_Dimension_Vector_Vec3.Vec3(v.value0 / v.value3, v.value1 / v.value3, v.value2 / v.value3);
};

// | Convert Vec3 to VecN
var vec3ToVecN = function (v) {
    return [ v.value0, v.value1, v.value2 ];
};

// | Extend 3D to 4D with w=one (point in homogeneous coordinates)
var vec3ToVec4 = function (dictSemiring) {
    var one = Data_Semiring.one(dictSemiring);
    return function (v) {
        return new Hydrogen_Schema_Dimension_Vector_Vec4.Vec4(v.value0, v.value1, v.value2, one);
    };
};

// | Project 3D to 2D (drop z)
var vec3ToVec2 = function (v) {
    return new Hydrogen_Schema_Dimension_Vector_Vec2.Vec2(v.value0, v.value1);
};

// | Convert Vec2 to VecN
var vec2ToVecN = function (v) {
    return [ v.value0, v.value1 ];
};

// ═══════════════════════════════════════════════════════════════════════════════
//                                                                // conversions
// ═══════════════════════════════════════════════════════════════════════════════
// | Extend 2D to 3D with z=zero
var vec2ToVec3 = function (dictSemiring) {
    var zero = Data_Semiring.zero(dictSemiring);
    return function (v) {
        return new Hydrogen_Schema_Dimension_Vector_Vec3.Vec3(v.value0, v.value1, zero);
    };
};
