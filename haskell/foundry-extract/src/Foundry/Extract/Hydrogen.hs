-- | All generated PureScript code
data GeneratedCode = GeneratedCode
  { gcColors     :: !Text
  , gcTypography :: !Text
  , gcSpacing    :: !Text
  , gcButtons    :: !Text
  , gcHero       :: !Text
  , gcTheme      :: !Text
  }
  deriving stock (Eq, Show)

-- ===========================================================================
-- Color Atoms
-- ===========================================================================

-- | Generate PureScript module for color atoms
generateColorAtoms :: Text -> Vector ColorAtomRec -> Text
generateColorAtoms domain colors = T.unlines $
  [ "-- | Color atoms for " <> domain
  , "-- | Generated by Foundry Brand Ingestion Engine"
  , "-- | DO NOT EDIT - regenerate from brand source"
  , "module Brand.Atoms.Colors where"
  , ""
  , "import Prelude"
  , ""
  , "-- ============================================================"
  , "-- COLOR PALETTE"
  , "-- ============================================================"
  , ""
  ] ++ V.toList (V.map colorToPS colors)

-- | Convert color atom to PureScript constant
colorToPS :: ColorAtomRec -> Text
colorToPS c = T.unlines
  [ "-- | " <> carName c <> " (" <> carRole c <> ")"
  , "-- | UUID5: " <> UUID.toText (carId c)
  , sanitizeName (carName c) <> " :: String"
  , sanitizeName (carName c) <> " = \"" <> carHex c <> "\""
  , ""
  ]

-- ===========================================================================
-- Font Atoms
-- ===========================================================================

-- | Generate PureScript module for font atoms
generateFontAtoms :: Text -> Vector FontAtomRec -> Text
generateFontAtoms domain fonts = T.unlines $
  [ "-- | Typography atoms for " <> domain
  , "-- | Generated by Foundry Brand Ingestion Engine"
  , "module Brand.Atoms.Typography where"
  , ""
  , "import Prelude"
  , ""
  , "-- ============================================================"
  , "-- FONT FAMILIES"
  , "-- ============================================================"
  , ""
  ] ++ V.toList (V.map fontToPS fonts)

-- | Convert font atom to PureScript
fontToPS :: FontAtomRec -> Text
fontToPS f = T.unlines
  [ "-- | " <> farFamily f <> " (" <> farRole f <> ")"
  , "-- | UUID5: " <> UUID.toText (farId f)
  , "font" <> T.toTitle (farRole f) <> " :: String"
  , "font" <> T.toTitle (farRole f) <> " = \"" <> farFamily f <> ", " <> fallback (farRole f) <> "\""
  , ""
  ]
  where
    fallback role
      | role == "mono" = "ui-monospace, monospace"
      | role == "heading" = "ui-sans-serif, system-ui, sans-serif"
      | otherwise = "ui-sans-serif, system-ui, sans-serif"

