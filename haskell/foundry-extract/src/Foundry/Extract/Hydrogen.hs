-- | Generate combined theme module with CSS variables
generateTheme :: ExtractedRegistry -> Text
generateTheme reg = T.unlines $
  [ "-- | Brand theme for " <> erDomain reg
  , "-- | Generated by Foundry Brand Ingestion Engine"
  , "module Brand.Theme where"
  , ""
  , "import Prelude"
  , ""
  , "-- ============================================================"
  , "-- CSS CUSTOM PROPERTIES"
  , "-- ============================================================"
  , ""
  , "-- | CSS to inject into document head"
  , "themeCss :: String"
  , "themeCss = \"\"\""
  , ":root {"
  ] <> V.toList (V.map colorToVar (erColors reg))
    <> V.toList (V.map fontToVar (erFonts reg))
    <> V.toList (V.map spacingToVar (erSpacing reg))
    <>
  [ "}"
  , "\"\"\""
  , ""
  , "-- ============================================================"
  , "-- TAILWIND CONFIG (for build integration)"
  , "-- ============================================================"
  , ""
  , "tailwindTheme :: String"
  , "tailwindTheme = \"\"\""
  , "{"
  , "  \"extend\": {"
  , "    \"colors\": {"
  ] <> V.toList (V.imap colorToTailwind (erColors reg))
    <>
  [ "    },"
  , "    \"fontFamily\": {"
  ] <> V.toList (V.imap fontToTailwind (erFonts reg))
    <>
  [ "    }"
  , "  }"
  , "}"
  , "\"\"\""
  ]

colorToVar :: ColorAtomRec -> Text
colorToVar c = "  --color-" <> sanitizeName (carName c) <> ": " <> carHex c <> ";"

fontToVar :: FontAtomRec -> Text
fontToVar f = "  --font-" <> farRole f <> ": \"" <> farFamily f <> "\";"

spacingToVar :: SpacingAtomRec -> Text
spacingToVar s = "  --spacing-" <> sarName s <> ": " <> T.pack (show (sarValue s)) <> "rem;"

colorToTailwind :: Int -> ColorAtomRec -> Text
colorToTailwind i c =
  "      \"" <> sanitizeName (carName c) <> "\": \"" <> carHex c <> "\"" <> comma
  where
    comma = if i < 5 then "," else ""  -- Simplified, would need actual count

fontToTailwind :: Int -> FontAtomRec -> Text
fontToTailwind i f =
  "      \"" <> farRole f <> "\": [\"" <> farFamily f <> "\", \"sans-serif\"]" <> comma
  where
    comma = if i < 2 then "," else ""

-- ===========================================================================
-- Full Generation
-- ===========================================================================

-- | Generate all PureScript modules from extracted registry
generateAll :: ExtractedRegistry -> GeneratedCode
generateAll reg = GeneratedCode
  { gcColors = generateColorAtoms (erDomain reg) (erColors reg)
  , gcTypography = generateFontAtoms (erDomain reg) (erFonts reg)
  , gcSpacing = generateSpacingAtoms (erDomain reg) (erSpacing reg)
  , gcButtons = generateButtonMolecule (erDomain reg) (erButtons reg)
  , gcHero = generateHeroCompound (erDomain reg) (erHeros reg)
  , gcTheme = generateTheme reg
  }

-- ===========================================================================
-- Helpers
-- ===========================================================================

-- | Sanitize name for PureScript identifier
sanitizeName :: Text -> Text
sanitizeName name = T.concatMap sanitizeChar name
  where
    sanitizeChar c
      | c == '-' = "_"
      | c == '.' = "_"
      | c == ' ' = "_"
      | otherwise = T.singleton c
