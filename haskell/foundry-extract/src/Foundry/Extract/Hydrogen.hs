-- | Convert hero compound to PureScript component
heroToPS :: HeroCompoundRec -> Text
heroToPS h = T.unlines
  [ "-- | Hero section"
  , "-- | UUID5: " <> UUID.toText (hcrId h)
  , "hero :: forall w i. { heading :: String, subheading :: String, cta :: HH.HTML w i } -> HH.HTML w i"
  , "hero { heading, subheading, cta } ="
  , "  HH.section"
  , "    [ cls [ \"py-24 bg-primary text-white\" ] ]"
  , "    [ container \"\""
  , "        [ column \"gap-6\""
  , "            [ HH.h1"
  , "                [ cls [ \"text-5xl font-heading font-bold\" ] ]"
  , "                [ HH.text heading ]"
  , "            , HH.p"
  , "                [ cls [ \"text-xl font-body opacity-90\" ] ]"
  , "                [ HH.text subheading ]"
  , "            , cta"
  , "            ]"
  , "        ]"
  , "    ]"
  , ""
  ]

-- ===========================================================================
-- Theme (CSS Variables)
