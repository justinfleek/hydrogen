-- | Convert spacing atom to PureScript
spacingToPS :: SpacingAtomRec -> Text
spacingToPS s = T.unlines
  [ "-- | spacing-" <> sarName s <> " (" <> T.pack (show (sarPx s)) <> "px)"
  , "-- | UUID5: " <> UUID.toText (sarId s)
  , "spacing" <> sanitizeName (sarName s) <> " :: String"
  , "spacing" <> sanitizeName (sarName s) <> " = \"" <> T.pack (show (sarValue s)) <> "rem\""
  , ""
  ]

-- ===========================================================================
-- Button Molecules
-- ===========================================================================

-- | Generate PureScript module for button molecules
generateButtonMolecule :: Text -> Vector ButtonMoleculeRec -> Text
generateButtonMolecule domain buttons = T.unlines $
  [ "-- | Button molecules for " <> domain
  , "-- | Generated by Foundry Brand Ingestion Engine"
  , "module Brand.Molecules.Button where"
  , ""
  , "import Prelude"
  , "import Halogen.HTML as HH"
  , "import Halogen.HTML.Properties as HP"
  , "import Hydrogen.UI.Core (cls)"
  , ""
  , "-- ============================================================"
  , "-- BUTTON VARIANTS"
  , "-- ============================================================"
  , ""
  ] ++ V.toList (V.map buttonToPS buttons)

-- | Convert button molecule to PureScript component
buttonToPS :: ButtonMoleculeRec -> Text
buttonToPS b = T.unlines
  [ "-- | " <> T.toTitle (bmrVariant b) <> " button"
  , "-- | UUID5: " <> UUID.toText (bmrId b)
  , "-- | Composed from: bg=" <> UUID.toText (bmrBgColor b) <> ", text=" <> UUID.toText (bmrTextColor b)
  , "button" <> T.toTitle (bmrVariant b) <> " :: forall w i. Array (HH.HTML w i) -> HH.HTML w i"
  , "button" <> T.toTitle (bmrVariant b) <> " children ="
  , "  HH.button"
  , "    [ cls"
  , "        [ \"inline-flex items-center justify-center\""
  , "        , \"px-4 py-2\""
  , "        , \"rounded-md\""
  , "        , \"font-medium\""
  , "        , \"transition-colors duration-150\""
  , "        , \"focus:outline-none focus:ring-2 focus:ring-offset-2\""
  , case bmrVariant b of
      "primary" -> "        , \"bg-primary text-white hover:bg-primary/90\""
      "secondary" -> "        , \"bg-white text-primary border border-primary hover:bg-primary/10\""
      _ -> "        , \"bg-gray-100 text-gray-900 hover:bg-gray-200\""
  , "        ]"
  , "    ]"
  , "    children"
  , ""
  ]

